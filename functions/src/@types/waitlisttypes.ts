/**
 * Interface representing the properties of a waitlist entry.
 *
 * @property {string} name - The name of the person on the waitlist.
 * @property {string} email - The email address of the person on the waitlist.
 * @property {string} phone - The phone number of the person on the waitlist.
 */
export interface WaitlistProperties {
    name: string;
    email: string;
    phone: string;
}
